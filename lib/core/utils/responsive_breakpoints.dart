abstract class ResponsiveBreakpoints {
  /// < 600px — móvil
  static const double mobile = 600;

  /// 600px – 1024px — tablet
  static const double tablet = 1024;

  /// 1024px – 1440px — desktop estándar
  static const double desktop = 1440;

  /// > 1440px — pantallas grandes (wide desktop / TV)
  static const double wideDesktop = 1920;

  /// Ancho máximo del contenido centrado en desktop
  static const double maxContentWidth = 1200;

  /// Ancho máximo de modales / diálogos
  static const double maxModalWidth = 480;
}